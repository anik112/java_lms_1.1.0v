    public void displayDatasWithinTable() {
        BookIssueService bookIssueService = new BookIssueService();
        List<BookIssue> pList = bookIssueService.getList();
        DefaultTableModel model = (DefaultTableModel) tblShowBookIssue.getModel();
        Object[] row = new Object[9];

        for (int i = 0; i < pList.size(); i++) {

            row[0] = pList.get(i).getId();
            row[1] = pList.get(i).getBookId();
            row[2] = pList.get(i).getStudentId();
            row[3] = pList.get(i).getIssueDate();
            row[4] = pList.get(i).getExpireDate();
            row[5] = pList.get(i).getReturnDate();
            row[6] = pList.get(i).getQty();
            row[7] = pList.get(i).getStatus();

            model.addRow(row);
        }

    }

    public void refreshTable() {
        DefaultTableModel model = (DefaultTableModel) tblShowBookIssue.getModel();
        model.setRowCount(0);
        displayDatasWithinTable();
    }

