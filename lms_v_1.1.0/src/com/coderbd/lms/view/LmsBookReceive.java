 public void displayList() {
        bookService = new BookService();
        Set<Book> cList = bookService.getBookList();

        cList.forEach((c) -> {
            txtBookId.addItem(c.getId() + ", " + c.getName());

        });

    }

    public void displayStudentList() {
        studentService = new StudentService();
        Set<Student> cList = studentService.getStudentList();

        cList.forEach((c) -> {
            txtStudentId.addItem(c.getId() + ", " + c.getName());

        });

    }

    public void displayDatasWithinTable() {
         bookReceiveService=new BookReceiveService();
        List<BookReceive> pList = bookReceiveService.getList();
        DefaultTableModel model = (DefaultTableModel) tblDisplay.getModel();
        Object[] row = new Object[5];

        for (int i = 0; i < pList.size(); i++) {

            row[0] = pList.get(i).getId();
            row[1] = pList.get(i).getBookId();
            row[2] = pList.get(i).getStudentId();
            row[3] = pList.get(i).getReturnDate();
            row[4] = pList.get(i).getStatus();

            model.addRow(row);
        }

    }

    public void refreshTable() {
        DefaultTableModel model = (DefaultTableModel) tblDisplay.getModel();
        model.setRowCount(0);
        displayDatasWithinTable();
    }
