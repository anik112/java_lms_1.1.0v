    private void btnReceiveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnReceiveActionPerformed
     
       
        int bid = Integer.parseInt(bookID);
        int stdID = Integer.parseInt(studentID);
        BookReceive rookReceive = new BookReceive(
                bid,
                stdID,
                (comboDayReturnDate.getSelectedItem().toString())
                +"/"+(comboMonthReturnDate.getSelectedItem().toString())
                +"/"+(comboYearReturnDate.getSelectedItem().toString()),
                Integer.parseInt(txtQty.getText()),
                txtStatus.getText()
        );

         bookReceiveService = new BookReceiveService();

        bookReceiveService.save(rookReceive);

        //for book summary
        
        Book book=bookService.getBookById(Integer.parseInt(bookID));
        
        
        
        bookSummaryService = new BookSummaryService();
  
        try {
            System.out.println("book.getBookCode()"+book.getBookCode());
            
       
            BookSummary bs = bookSummaryService.getBookSummaryByBookCode(book.getBookCode());
            if (bs != null) {
           //     if (bs.getBookCode().equals(book.getBookCode())) {



                    // after modification
                  
                    int nowAvailableQty = bs.getAvilableStock() + Integer.parseInt(txtQty.getText());
                    int nowAlreadyQty = bs.getAlreadyLend() - Integer.parseInt(txtQty.getText());
                    BookSummary bsx = new BookSummary(bs.getTotalStock(), nowAvailableQty, nowAlreadyQty);
                    bsx.setId(bs.getId());

                    bookSummaryService.update(bsx);

                    refreshTable();

             //   } 
            }
        } catch (Exception e) {
           e.printStackTrace();

        }

        refreshTable();
    }//GEN-LAST:event_btnReceiveActionPerformed

