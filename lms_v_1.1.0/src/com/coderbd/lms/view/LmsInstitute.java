    public void displayInstituteWithinTable() {
        instituteService = new InstituteService();
        List<Institute> pList = instituteService.getList();
        DefaultTableModel model = (DefaultTableModel) tblShowInstituteInformation.getModel();
        Object[] row = new Object[11];

        for (int i = 0; i < pList.size(); i++) {
            row[0] = pList.get(i).getId();
            row[1] = pList.get(i).getName();
            row[2] = pList.get(i).getHouseNo();
            row[3] = pList.get(i).getRoadNo();
            row[4] = pList.get(i).getBlockOrVillage();
            row[5] = pList.get(i).getThana();
            row[6] = pList.get(i).getDistrict();
            row[7] = pList.get(i).getDivision();
            row[8] = pList.get(i).getCountry();
            row[9] = pList.get(i).getAddresstype();
            row[10] = pList.get(i).getInstituteType();
            
            //System.out.println("  *  ");
            
            model.addRow(row);
        }

    }
    
    
    public void refreshTable() {
        DefaultTableModel model = (DefaultTableModel) tblShowInstituteInformation.getModel();
        model.setRowCount(0);
        displayInstituteWithinTable();
    }
    
