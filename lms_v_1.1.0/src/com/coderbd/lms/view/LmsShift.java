    public void displayDatasWithinTable() {
        as = new ShiftService();
        List<Shift> pList = as.getShifts();
        DefaultTableModel model = (DefaultTableModel) tblDisplay.getModel();
        Object[] row = new Object[5];

        for (int i = 0; i < pList.size(); i++) {
            row[0] = pList.get(i).getId();
            row[1] = pList.get(i).getName();
            row[2] = pList.get(i).getStartingTime();
            row[3] = pList.get(i).getEndingTime();
            row[4] = pList.get(i).getStatus();
            model.addRow(row);
        }

    }

    public void refreshTable() {
        DefaultTableModel model = (DefaultTableModel) tblDisplay.getModel();
        model.setRowCount(0);
        displayDatasWithinTable();
    }

