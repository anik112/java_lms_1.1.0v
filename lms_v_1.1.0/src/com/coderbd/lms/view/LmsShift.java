    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        String name = txtName.getText();
        String startingTime = txtName.getText();
        String endingTime = txtName.getText();
        String status = "";
        if (rfalse.isSelected()) {
            status = rfalse.getText();
        } else {
            status = rTrue.getText();
        }

        Shift user = new Shift(name, startingTime, endingTime, status);
        as = new ShiftService();
        as.save(user);
        lblMsg.setText("1 Record Successfully Saved!");
        refreshTable();
    }//GEN-LAST:event_btnAddActionPerformed

    private void tblDisplayMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblDisplayMouseClicked
        int i = tblDisplay.getSelectedRow();
        TableModel model = tblDisplay.getModel();
        txtName.setText(model.getValueAt(i, 1).toString());
        txtStartingTime.setText(model.getValueAt(i, 2).toString());
        txtEnding.setText(model.getValueAt(i, 3).toString());
        String status = model.getValueAt(i, 4).toString();
        if (status.equals("True")) {
            rTrue.setSelected(true);
        }
        if (status.equals("False")) {
            rfalse.setSelected(true);
        }
        
       
    }//GEN-LAST:event_tblDisplayMouseClicked

    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        String name = txtName.getText();
        String startingTime = txtName.getText();
        String endingTime = txtName.getText();
        String status = "";
        if (rfalse.isSelected()) {
            status = rfalse.getText();
        } else {
            status = rTrue.getText();
        }

        Shift user = new Shift(name, startingTime, endingTime, status);
        int i = tblDisplay.getSelectedRow();
        TableModel model = tblDisplay.getModel();
        user.setId(Integer.parseInt(model.getValueAt(i, 0).toString()));
        as = new ShiftService();
        as.update(user);
        lblMsg.setText("1 Record Successfully Updated!");
        refreshTable();
    }//GEN-LAST:event_btnEditActionPerformed

    private void btnDelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDelActionPerformed
        as = new ShiftService();
        int i = tblDisplay.getSelectedRow();
        TableModel model = tblDisplay.getModel();
        int id = Integer.parseInt(model.getValueAt(i, 0).toString());
        as.delete(id);
        lblMsg.setText("1 Record Successfully Deleted!");
        refreshTable();
    }//GEN-LAST:event_btnDelActionPerformed

